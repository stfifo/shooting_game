// ── Pixel Art Sprite Engine ──────────────────────────────────────
const PX=3; // pixel scale

function pxDraw(data,pal,ox,oy,flipY=false,tint=null){
  const rows=data.length, cols=data[0].length;
  const x0=Math.round(ox-cols*PX/2), y0=Math.round(oy-rows*PX/2);
  for(let r=0;r<rows;r++){
    for(let c=0;c<cols;c++){
      const ci=data[r][c]; if(!ci)continue;
      cx.fillStyle=tint||pal[ci];
      cx.fillRect(x0+c*PX, y0+(flipY?rows-1-r:r)*PX, PX, PX);
    }
  }
}

